package ex_13072024;

public class Lab041 {
    public static void main(String[] args) {
        //relational operators  -> boolean
        // //>, < >=, <=, ==, != ( ! = ) - true or false
        int age_mamta = 30;
        int age_pramod = 34;
        boolean result = age_pramod > age_mamta;
        boolean result2 = age_pramod < age_mamta;
        boolean result3 = age_pramod >= age_mamta; // OR gate
        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
    }
}
